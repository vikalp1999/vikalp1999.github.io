import Contact from "./components/contact/contact";

function App() {
  const [load, upadateLoad] = useState(true);

  useEffect(() => {
    const timer = setTimeout(() => {
      upadateLoad(false);
    }, 1200);

    return () => clearTimeout(timer);
  }, []);

  return (
    <Router>
      <Preloader load={load} />
      <div className="App" id={load ? "no-scroll" : "scroll"}>
        <Navbar />
        <ScrollToTop />
        <Home/>
        <Home2 />
         <About/>
        <Projects/>
        <Contact/>
        <Navigator/>
        <Footer />
       
      </div>
    </Router>
  );
}

export default App;
